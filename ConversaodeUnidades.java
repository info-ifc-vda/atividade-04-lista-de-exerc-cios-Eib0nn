public class ConversaodeUnidades {

    public static double litrosParaCm3(double litros) {
        return litros * 1000;
    }

    public static double m3ParaLitros(double m3) {
        return m3 * 1000;
    }

    public static double m3ParaPesCubicos(double m3) {
        return m3 * 35.32;
    }

    public static double galaoAmericanoParaPol3(double galao) {
        return galao * 231;
    }

    public static double galaoAmericanoParaLitros(double galao) {
        return galao * 3.785;
    }
}
